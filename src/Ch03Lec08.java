public class Ch03Lec08 {
    public static void main(String[] args) {
        // Type casting

        //1. Widening Casting (implicit Casting)

        double d = 4.5f;
        System.out.println(d);

        //2. Narrowing Casting (Explicit Casting)

        int  i = (int) 4.46f;
        System.out.println(i);
    }
}
